
public class trio<L,M,R> {
	private L l;
	private M m;
    private R r;
    public trio(L l, M m, R r){
        this.l = l;
        this.m = m;
        this.r = r;
    }
    public L getL(){ return l; }
    public M getM(){ return m; }
    public R getR(){ return r; }
    public void setL(L l){ this.l = l; }
    public void setM(M m){ this.m = m; }
    public void setR(R r){ this.r = r; }

}
